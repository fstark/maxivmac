/* Return the localised / config-derived string for a
   substitution escape code (e.g. ^p = app name). */
const char * GetSubstitutionStr(char x)
{
	const char *s;

	switch (x) {
		case 'w':
			s = kStrHomePage;
			break;
		case 'y':
			s = kStrCopyrightYear;
			break;
		case 'p':
			s = kStrAppName;
			break;
		case 'v':
			s = kAppVariationStr;
			break;
		case 'r':
			s = const_cast<char*>(g_machine->config().romFileName);
			break;
		case 'c':
			s = kControlModeKey;
			break;
		case 'm':
			s = kUnMappedKey;
			break;
		case 'k':
			if (g_controlKeyPressed) {
				s = Localize(kStrPressed);
			} else {
				s = Localize(kStrReleased);
			}
			break;
		case 'g':
			if (g_wantMagnify) {
				s = Localize(kStrOn);
			} else {
				s = Localize(kStrOff);
			}
			break;
		case 'f':
			if (g_wantFullScreen) {
				s = Localize(kStrOn);
			} else {
				s = Localize(kStrOff);
			}
			break;
		case 'b':
			if (g_runInBackground) {
				s = Localize(kStrOn);
			} else {
				s = Localize(kStrOff);
			}
			break;
		case 'h':
			if (g_speedStopped) {
				s = Localize(kStrStoppedOn);
			} else {
				s = Localize(kStrStoppedOff);
			}
			break;
		case 'l':
			if (g_wantNotAutoSlow) {
				s = Localize(kStrStoppedOff);
			} else {
				s = Localize(kStrStoppedOn);
			}
			break;
		case 's':
			switch (g_speedValue) {
				case 0:
					s = "1x";
					break;
				case 1:
					s = "2x";
					break;
				case 2:
					s = "4x";
					break;
				case 3:
					s = "8x";
					break;
				case 4:
					s = "16x";
					break;
				case 5:
					s = "32x";
					break;
				default:
					s = Localize(kStrSpeedValueAllOut);
					break;
			}
			break;
		default:
			s = "???";
			break;
	}
	return s;
}

int ClStrSizeSubstCStr(const char *s)
{
	/* must match ClStrAppendSubstCStr ! */

	const char *p = s;
	char c;
	int L = 0;

	while (0 != (c = *p++)) {
		if ('^' == c) {
			if (0 == (c = *p++)) {
				goto l_exit; /* oops, unexpected end of string, abort */
			} else if ('^' == c) {
				++L;
			} else {
				L += ClStrSizeSubstCStr(GetSubstitutionStr(c));
			}
		} else if (';' == c) {
			if (0 == (c = *p++)) {
				goto l_exit; /* oops, unexpected end of string, abort */
			}

			switch (c) {
				case 'l':
					if (0 == (c = *p++)) {
						/* oops, unexpected end of string, abort */
						goto l_exit;
					}
					(void) c; /* not needed for computing size */
					break;
				default:
					break;
			}
			++L;
		} else {
			++L;
		}
	}

l_exit:
	return L;
}

void ClStrAppendChar(int *L0, uint8_t *r, uint8_t c)
{
	int L = *L0;

	r[L] = c;
	L++;
	*L0 = L;
}

